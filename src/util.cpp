#include "util.hpp"

using namespace std;

namespace mandelbrot
{

RGB HSVtoRGB(double h, double s, double v)
{
    double r, g, b; 
    int i;
    double f, p, q, t;
    if( s == 0 ) {
        return RGB(v); // achromatic (grey)
    }
    h /= 60;            // sector 0 to 5
    i = floor( h );
    f = h - i;          // factorial part of h
    p = v * ( 1 - s );
    q = v * ( 1 - s * f );
    t = v * ( 1 - s * ( 1 - f ) );
    switch( i ) {
        case 0:
            r = v;
            g = t;
            b = p;
            break;
        case 1:
            r = q;
            g = v;
            b = p;
            break;
        case 2:
            r = p;
            g = v;
            b = t;
            break;
        case 3:
            r = p;
            g = q;
            b = v;
            break;
        case 4:
            r = t;
            g = p;
            b = v;
            break;
        default:        // case 5:
            r = v;
            g = p;
            b = q;
            break;
    }

    return RGB(r, g, b);
}

}